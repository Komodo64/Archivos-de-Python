# Definimos dos números de ejemplo
a = 10
b = 3

print("Operaciones aritméticas con a =", a, "y b =", b)
print("Suma: a + b =", a + b)
print("Resta: a - b =", a - b)
print("Multiplicación: a * b =", a * b)
print("División: a / b =", a / b)
print("División entera: a // b =", a // b)
print("Módulo (resto): a % b =", a % b)
print("Potencia: a ** b =", a ** b)
