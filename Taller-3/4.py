#4. Tarea: Tienes una lista con las temperaturas de la semana. Calcula y muestra el promedio de las
#temperaturas.
#temperaturas = [22.5, 24.0, 21.8, 25.1, 23.9, 22.7, 21.5]

# Lista de temperaturas de la semana
temperaturas = [22.5, 24.0, 21.8, 25.1, 23.9, 22.7, 21.5]

# Calcular el promedio
promedio = sum(temperaturas) / len(temperaturas)

# Mostrar resultado
print("El promedio de las temperaturas es:", promedio)

